	/* parseTimeCharacter 
	 * given a character, return the corresponding bit array
	 * only search for characters used to display time. 
	 */
	this.parseTimeCharacter = function(character) {
		if( character === ":" ) {
			return this.colon;
		} else if ( character === " " ) {
			return this.space;
		} else if( character >= '0' && character <= '9' ) {
			return this["_" + character];	
		}
	};
	
	/* displayTime 
	 * given a timeString formatted by moment.js as "12:34 pm", 
	 * parse through the time and return the correctly formatted bit array
	 */
	this.displayTime = function(timeString) {
		if(timeString) {
			var is_pm = !!(timeString.indexOf("pm") > -1);
			var formattedTime = "";
			
			if(timeString[0] === "0") { 
				// Time is before 9 - only 3 digits = 8:26 PM
				formattedTime = "   " + timeString[1] + "  :  " + timeString[3] + " " + timeString[4] + "   "; 
			} else {
				// Time is after 9 - 4 digits - 1st char is always 1 = 12:34 AM
				formattedTime = timeString[0] + timeString[1] + " : " + timeString[3] + " " + timeString[4] + "   "; 
			}

			var result = [];
			for(i = 0; i < formattedTime.length; i++ ) {
				result = this.parseTimeCharacter(formattedTime[i]).concat(result);
			}
			result = (is_pm ? this.pm : this.am).concat( result );
			return result;
		}
	};
